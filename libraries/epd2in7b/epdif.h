#ifndef EPDIF_H
#define EPDIF_H

#include <arduino.h>
#include <pins_arduino.h>
// Pin definition
#define RST_PIN         D2 // D2 8
#define DC_PIN          D4 // D4 9
#define CS_PIN          D8 // D8 10
#define BUSY_PIN        D3 // D1 7

class EpdIf {
public:
    EpdIf(void);
    ~EpdIf(void);

    static int  IfInit(bool b3Wire);
    static void DigitalWrite(int pin, int value); 
    static int  DigitalRead(int pin);
    static void DelayMs(unsigned int delaytime);
    static void SpiTransfer(unsigned char data); // 4 wire SPI
	static void SpiTransfer(unsigned char data, bool isCommand); // 3 wire SPI 02-July-2018
};

#endif
