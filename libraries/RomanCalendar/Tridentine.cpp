	//fprintf(fp, "%4d,%02d-%02d-%4d,\"%s\",\"%s\",\"%s\",\"%s\"\n", year, ::day(datetime), ::month(datetime), ::year(datetime), Class.c_str(), Colour.c_str(), Mass.c_str(), Commemoration.c_str());
