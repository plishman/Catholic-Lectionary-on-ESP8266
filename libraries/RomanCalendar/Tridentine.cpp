int Tridentine::liturgical_year(time64_t datetime) {
	int _year = ::year(datetime);
	if (datetime >= first_advent_sunday(_year) && datetime < date(1, 1, ::year(first_advent_sunday(_year)) + 1)) {
		return _year + 1;
	}

	return _year;
}

